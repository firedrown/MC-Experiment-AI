package marlon.minecraftai.ai.task.error;

/**
 * Displays a simple error message to the user.
 *
 */
public class StringTaskError extends TaskError {

	private final String message;

	public StringTaskError(String message) {
		super(message);
		this.message = message;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + (message == null ? 0 : message.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final StringTaskError other = (StringTaskError) obj;
		if (message == null) {
			if (other.message != null) {
				return false;
			}
		} else if (!message.equals(other.message)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "StringTaskError [message=" + message + "]";
	}
}
